export default class BossFightState extends State {
    
    constructor(playerHealth = 3){
        super();
        this.playerHealth = playerHealth;
    }

    init(core){
        this.drawLoadingScreen(core);

        return new Promise((resolve, reject) => {
            core.assets.loadImages({
                tiles: "./assets/office_tiles.png",
                player: "./assets/player.png",
                enemy: "./assets/enemy.png",
                projectile: "./assets/projectile.png"
            })
            .then(() => {
                core.camera.resetPos();

                this.tilemap = new Tilemap(30, 20, 16, core.assets.loaded.tiles);
                this.tilemap.loadMap(map);

                this.player = new PlayerEntity(32, 260, core.assets.loaded.player, this.playerHealth);

                this.boss = new BossEntity(448, 270, core.assets.loaded.enemy, core.assets.loaded.projectile, [
                    {x: 448, y: 270}, {x: 16, y: 270}, {x: 232, y: 270}
                ]);

                resolve();
            })
            .catch(reject);
        });
    }

    update(dt, core){
        // if the player is dead, don't update anything, just wait for the enter key
        if(this.player.dead){
            if(core.input.keys.Enter) core.stateManager.switchState(new BossFightState());
            return true;
        }

        if(this.boss.dead){
            core.stateManager.switchState(new FinishState());
            return true;
        }

        this.boss.update(dt, core, this.player);

        this.player.update(dt, core, this.tilemap);

        if(this.player.collidesWith(this.boss)) this.player.onCollideWithEntity(this.boss);
    }

    draw(core){
        //draw background
        core.ctx.fillStyle = "#949494";
        core.ctx.fillRect(0, 0, core.width, core.height);

        this.tilemap.drawArea(core, core.camera.x, core.camera.y, core.camera.width, core.camera.height);

        this.boss.draw(core);
        this.player.draw(core);

        // hud
        core.ctx.fillStyle = "#000";
        core.ctx.fillRect(0, 0, 48, 18);
        core.ctx.fillStyle = "#FFF";
        core.ctx.font = "16px sans-serif";
        core.ctx.fillText("HP: " + this.player.health, 1, 16);

        // game over text
        if(this.player.dead){
            core.ctx.fillStyle = "#ff0000";
            core.ctx.font = "32px sans-serif";
            core.ctx.fillText("GAME OVER.", (core.width / 3) - 16, (core.height / 2) - 32);
            core.ctx.font = "16px sans-serif";
            core.ctx.fillText("Press ENTER to retry.", core.width / 3, core.height / 2);
        }
    }

}